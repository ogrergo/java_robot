package donnees;

/**
 * <b><code>State</code> est l'énumaration permettant de representer les différents 
 * états d'un robot</b>
 * <p>
 * Les différents états sont :
 * <ul>
 * <li>BUSY</li>
 * <li>AVAILABLE</li>
 * </ul>
 * </p>
 * 
 * @author Lucas Bchini, Robin Jean, Louis van Beurden
 */
public enum State {
	BUSY(),
	AVAILABLE;
}
